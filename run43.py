#تابعی ایجاد کنید که دو آرگومان “قیمت اصلی” و “درصد تخفیف” را به صورت اعداد صحیح بگیرد و قیمت نهایی را پس از تخفیف برگرداند.

def dis(price, sale):
    res = price - price * sale /100
    if res %1 == 0:
        return (int(res))
    else :
        return round(res, 2)
    
print(dis(1500, 50))
print(dis(89, 20))
print(dis(100, 75))

